//求两数之和程序2号

#include <stdio.h>                 //注明输入/输出函数使用的头文件
void main()                        //定义主函数 
{
	int x,y,sum;                   //定义变量 
	scanf("%d%d",&x,&y);           //调用库函数从键盘输入数据	 
	sum=x+y;                       //计算两数之和  
	printf("sum=%d\n",sum);        //在屏幕上显示结果             
}
